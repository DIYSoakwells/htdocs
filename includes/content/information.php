    	<h4><b>INSTALLATION TIPS</b></h4>
    
    	<br/>
    
		<p>Our general advice on purchasing the correct stormwater system is to calculate the volume of the soakwells based on the roof area (catchment area) and the formula for predicted rainfall during a storm from your local or nearby council. Our calculator makes this easy.</p>
	
		<br/>
		
		<h5>LOCATION</h5>
	
		<p>When choosing a location, the soakwells should be at least 1.5 - 1.8m from footings and boundaries, depending on your council. Never start digging until you know where all your underground services are (sewer / gas / power / water / phone / fibre etc). </p>
		
		<p>You can contact <a href="http://www.1100.com.au/">Dial Before You Dig</a> for free to find out about the udergroud services on public land and we have some tips for you as well.</p>
	
		<br/>
	
		<h5>POLYPROPYLENE SOAKWELLS</h5>
	
		<p>Polypropylene soakwells should be side by side in rows and grouped based on just two or three sections of roof. This is important because downpipes can take different amounts of water from the roof due to the fall of the gutters, direction of the wind/rain and maintenance issues.</p>
		
		<p>We always aim for just two or three locations. Typically a row of polypropylene soakwells at the front and back of the house, depending on the available space and location of underground services. You need a minimum of 300mm cover from the top of the soakwell to ground level.</p>
	
		<p>For example: Four 140 litre soakwells = 2m long, 500mm wide. TOTAL depth 900mm (570mm soakwells plus 300mm cover = 870mm rounded up).</p>
	
		<br/>
	
		<h5>PIPING</h5>
	
		<p>For the piping, walk around your house and draw a rough plan of the boundary, house perimeter, downpipes and proposed soakwell locations.</p>
		
		<p>Pace out the length of pipe required from the furthest downpipe to the soakwells. The fall for the pipe is 100mm for every 10 metres. Cut a hole in the top of the soakwell for the pipe. <b>Make sure you glue the fittings and pipe.</b></p>
		
		<p>You should use grates under the downpipes to prevent water rising up the downpipes in any situation and to allow the system to breathe for optimal flow.</p>

		<br/>

		<h5>SAND REMOVAL</h5>
   	
		<p>Don't forget you will have the sand displaced by the soakwells at the end. To estimate the amount take the total volume of the polypropylene soakwells and add another 30%. We have some tips on how to deal with this as well.</p>
   
